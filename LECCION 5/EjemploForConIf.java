/**
* Bucle for: ejemplo decreciente de 10 a 1 con if
*
* @author Elena TÃ©llez
*/
public class EjemploForConIf {
	public static void main(String[] args) {
		for (int i = 10; i > 0; i--) {
			if (i < 7) {
			System.out.println("hola"); //inserto if con bucle
			}
		System.out.println(i); //cuenta hacia atras
		}
	}
}
